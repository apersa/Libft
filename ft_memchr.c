#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	char	*p;

	p = (char*)(s);
	while (n != 0)
	{
		if (*p != (char)c)
			p++;
		else
			return (p);
		n--;
	}
	return (NULL);
}
